      OS%SEND_BUFFER_REAL(ICG) = AC%VAL(ICOL)
#ifdef WITH_SCARC_DEBUG2
WRITE(MSG%LU_DEBUG,'(A,4I8,E14.6)') 'PACK_MATRIX_DIAGS: IOR0, ICG, ICW, ICOL, VAL:', &
                                     IOR0, ICG, ICW, ICOL, AC%VAL(ICOL)
#endif
   ENDDO
ENDDO

