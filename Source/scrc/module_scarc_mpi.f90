      IF (G%ZONES_LOCAL(ICW) /= 0) THEN
         OS%SEND_BUFFER_INT(LL) = G%ZONES_LOCAL(ICE)
      ELSE
         OS%SEND_BUFFER_INT(LL) = 0
      ENDIF
      LL = LL + 1
   ENDDO
ENDDO
