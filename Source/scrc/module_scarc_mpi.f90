INTEGER :: IOR0, ICG, ICW, ICOL

AC => SCARC_POINT_TO_CMATRIX(G, NTYPE)

OS%SEND_BUFFER_REAL = NSCARC_ZERO_REAL_EB
DO IOR0 = -3, 3
   IF (OL%GHOST_LASTW(IOR0) == 0) CYCLE
   DO ICG = OL%GHOST_FIRSTW(IOR0), OL%GHOST_LASTW(IOR0)
      ICW = OG%ICG_TO_ICW(ICG, 1)
